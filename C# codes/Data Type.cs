using System;

namespace Data_Type
{
    class Program
    {
        static void Main(string[] args)
        {
            // Type of data
            string phrase; // String
            char grade; // Character
            int age; // Integer
            double gpa;
            bool is_male;

            //Value
            phrase = "Owl House";
            grade = 'A';
            age = 30;
            gpa = 3.0;
            is_male = true;
            
            // Terminal Output
            Console.WriteLine(phrase);
            Console.WriteLine(grade);
            Console.WriteLine(age);
            Console.WriteLine(gpa);
            Console.WriteLine(is_male);
            Console.WriteLine();
        }
    }
}
